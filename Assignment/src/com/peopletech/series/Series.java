package com.peopletech.series;

public class Series {

	public static void main(String[] args) {
		
		int a1=1,a2=2,a3=3,a4=4,sum=0;
	
			
		for(int j=4;j<100;j++){

			System.out.print(a1+ " ");
			System.out.print(a2+" ");
			System.out.print(a3+" ");
			System.out.print(a4+" ");
			sum=a1+a2+a3+a4;
			System.out.print((sum)+" ");
			
		++a4;
		System.out.print(a4+" ");
		
		}

}
}
